#!/bin/bash
# StarOS Device Verification Script
# ОБЯЗАТЕЛЬНО запустите перед прошивкой!

set -e

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

echo "╔════════════════════════════════════════════════════════════╗"
echo "║         StarOS Device Verification v1.0                   ║"
echo "║         КРИТИЧЕСКАЯ ПРОВЕРКА ПЕРЕД ПРОШИВКОЙ              ║"
echo "╚════════════════════════════════════════════════════════════╝"
echo ""

# Проверка fastboot
if ! command -v fastboot &> /dev/null; then
    echo -e "${RED}❌ ОШИБКА: fastboot не установлен${NC}"
    exit 1
fi

echo -e "${GREEN}✅ fastboot установлен${NC}"

# Проверка подключения
echo "Проверка подключения устройства..."

if ! fastboot devices 2>&1 | grep -q "fastboot"; then
    echo -e "${RED}❌ ОШИБКА: Устройство не подключено в режиме fastboot${NC}"
    echo ""
    echo "Инструкция:"
    echo "  1. Выключите телефон"
    echo "  2. Зажмите Volume Down + Power"
    echo "  3. Дождитесь FASTBOOT"
    echo "  4. Подключите USB"
    exit 1
fi

echo -e "${GREEN}✅ Устройство подключено${NC}"
echo ""

# Получение информации
PRODUCT=$(fastboot getvar product 2>&1 | grep "product:" | awk '{print $2}' | tr -d '\r')
DEVICE=$(fastboot getvar device 2>&1 | grep "device:" | awk '{print $2}' | tr -d '\r')
HARDWARE=$(fastboot getvar hardware 2>&1 | grep "hardware:" | awk '{print $2}' | tr -d '\r')

echo "╔════════════════════════════════════════════════════════════╗"
echo "║                 ИНФОРМАЦИЯ ОБ УСТРОЙСТВЕ                  ║"
echo "╠════════════════════════════════════════════════════════════╣"
echo "║ Product:  $PRODUCT"
echo "║ Device:   $DEVICE"
echo "║ Hardware: $HARDWARE"
echo "╚════════════════════════════════════════════════════════════╝"
echo ""

# Проверка поддержки
SUPPORTED=false
TESTED=false
RISK_LEVEL=""

if [ "$PRODUCT" = "santoni" ] || [ "$DEVICE" = "santoni" ]; then
    echo "╔════════════════════════════════════════════════════════════╗"
    echo "║  ⚠️  УСТРОЙСТВО: Xiaomi Redmi 4X (santoni)               ║"
    echo "╠════════════════════════════════════════════════════════════╣"
    echo "║  Статус:      КОД ГОТОВ, НЕ ТЕСТИРОВАН                    ║"
    echo "║  SoC:         Qualcomm Snapdragon 435                     ║"
    echo "║  Тестирование: ❌ НЕ ПРОВЕРЕНО НА ЖЕЛЕЗЕ                   ║"
    echo "║  Риск брика:  🟡 СРЕДНИЙ                                   ║"
    echo "╚════════════════════════════════════════════════════════════╝"
    echo ""
    echo -e "${YELLOW}⚠️  КРИТИЧЕСКОЕ ПРЕДУПРЕЖДЕНИЕ:${NC}"
    echo ""
    echo "Код для Redmi 4X написан и скомпилирован, но:"
    echo "  ❌ НЕ ТЕСТИРОВАН на реальном устройстве"
    echo "  ❌ Могут быть проблемы с железом"
    echo "  ❌ Возможен брик устройства"
    echo ""
    SUPPORTED=true
    TESTED=false
    RISK_LEVEL="MEDIUM"
    
elif [ "$PRODUCT" = "davinci" ] || [ "$DEVICE" = "davinci" ]; then
    echo "╔════════════════════════════════════════════════════════════╗"
    echo "║  ⚠️  УСТРОЙСТВО: Xiaomi Mi 9T (davinci)                  ║"
    echo "╠════════════════════════════════════════════════════════════╣"
    echo "║  Статус:      КОД ГОТОВ, НЕ ТЕСТИРОВАН                    ║"
    echo "║  SoC:         Qualcomm Snapdragon 730                     ║"
    echo "║  Тестирование: ❌ НЕ ПРОВЕРЕНО НА ЖЕЛЕЗЕ                   ║"
    echo "║  Риск брика:  🔴 ВЫСОКИЙ                                   ║"
    echo "║  Pop-up камера: ⚠️  МОЖЕТ ПОВРЕДИТЬСЯ                     ║"
    echo "╚════════════════════════════════════════════════════════════╝"
    echo ""
    echo -e "${RED}⚠️  КРИТИЧЕСКОЕ ПРЕДУПРЕЖДЕНИЕ:${NC}"
    echo ""
    echo "Код для Mi 9T написан и скомпилирован, но:"
    echo "  ❌ НЕ ТЕСТИРОВАН на реальном устройстве"
    echo "  ❌ Pop-up камера может повредиться"
    echo "  ❌ Могут быть проблемы с железом"
    echo "  ❌ ВЫСОКАЯ вероятность брика"
    echo ""
    SUPPORTED=true
    TESTED=false
    RISK_LEVEL="HIGH"
    
else
    echo "╔════════════════════════════════════════════════════════════╗"
    echo "║  ❌ УСТРОЙСТВО НЕ ПОДДЕРЖИВАЕТСЯ!                         ║"
    echo "╚════════════════════════════════════════════════════════════╝"
    echo ""
    echo -e "${RED}Ваше устройство: $PRODUCT / $DEVICE${NC}"
    echo ""
    echo "Поддерживаемые устройства:"
    echo "  ⚠️  santoni (Xiaomi Redmi 4X) - КОД ГОТОВ, НЕ ТЕСТИРОВАН"
    echo "  ⚠️  davinci (Xiaomi Mi 9T) - КОД ГОТОВ, НЕ ТЕСТИРОВАН"
    echo ""
    echo -e "${RED}⚠️  ПРОШИВКА НЕПОДДЕРЖИВАЕМОГО УСТРОЙСТВА = 100% БРИК!${NC}"
    exit 1
fi

# Финальное подтверждение
echo "╔════════════════════════════════════════════════════════════╗"
echo "║              ФИНАЛЬНОЕ ПОДТВЕРЖДЕНИЕ                       ║"
echo "╚════════════════════════════════════════════════════════════╝"
echo ""
echo "Вы подтверждаете, что:"
echo "  [ ] Это ТЕСТОВОЕ устройство (не основной телефон)"
echo "  [ ] У вас есть РЕЗЕРВНОЕ устройство"
echo "  [ ] Вы умеете восстанавливать через EDL"
echo "  [ ] Вы скачали официальную прошивку MIUI"
echo "  [ ] Вы сделали backup через TWRP"
echo "  [ ] Вы готовы к ПОЛНОЙ ПОТЕРЕ устройства"
echo "  [ ] Вы понимаете, что код НЕ ТЕСТИРОВАН"
echo ""

read -p "Подтверждаю всё вышеперечисленное (yes/NO): " final_confirm
if [ "$final_confirm" != "yes" ]; then
    echo ""
    echo -e "${RED}❌ Прошивка отменена${NC}"
    echo ""
    echo "Мудрое решение! Дождитесь тестирования на реальном железе."
    exit 1
fi

echo ""
echo "╔════════════════════════════════════════════════════════════╗"
echo "║  ⚠️  ПРОВЕРКА ЗАВЕРШЕНА                                   ║"
echo "║  ⚠️  ПРОШИВКА НА ВАШ СТРАХ И РИСК                         ║"
echo "╚════════════════════════════════════════════════════════════╝"
echo ""
echo -e "${YELLOW}Устройство: $PRODUCT${NC}"
echo -e "${YELLOW}Риск: $RISK_LEVEL${NC}"
echo -e "${YELLOW}Тестирование: НЕТ${NC}"
echo ""
